    <h1>Oferta educationala<br></h1>
    <p> <b>Studii universitare în Informatica</b> </p>

    <p class="p1"> <a href="http://inf.ucv.ro/documents/pliant-admitere-2019.pdf">Pliant admitere</a> </p>
    <p><b>Cursuri de specialitate</b> </p>

    <img src="img1.png" class="img1" alt="poza1">
    <p>Studierea informaticii este provocatoare, captivanta si impune aplicarea notiunilor teoretice
        dobândite anterior. Cunoasterea informaticii si aplicabilitatea sa reprezinta un atu pentru
        integrarea garantata si rapida pe piata muncii.</p>
    <p>Sistemul de predare este organizat în conformitate cu modelul Bologna: dupa absolvirea studiilor de
        Licenta în Informatica, studentii care intentioneaza sa-si continue studiile pot alege cursurile de
        Master în Informatica - Metode si Modele în Inteligenta Artificiala/Tehnologii Avansate de
        Prelucrare a Informatiei(in limba engleza)</p>



    <p> <b>O oferta de predare în strânsa legatura cu piata muncii </b> </p>

    <img src="img2.png" class="img1" alt="poza2">
    <p>Studentii de la specializarea Informatica desfasoara, în cadrul primului ciclu de studii, un stagiu
        de practica la o companie de profil si încheie acest stagiu cu o lucrare de absolvire ce poate fi
        realizata în cotutela unei companii IT partenere. Actualmente, exista aproximativ 15 companii
        partenere ce includ principalii angajatori IT din Craiova si nu numai.</p>
    <p>Unele dintre companiile cele mai calificate în domeniul IT au decis sa colaboreze activ cu
        Departamentul de Informatica prin participarea la mese rotunde, unde cadrele didactice si
        profesionisti din industrie stabilesc un dialog constructiv reducând, astfel, timpii de reactie.
        Companiile care au aderat la aceasta initiativa ofera consultanta de specialitate privind oferta
        noastra educationala, contribuie la dezvoltarea si punerea în aplicare a traseelor de formare
        educationala si sunt implicate activ în procesul de integrare pe piata muncii a absolventilor cu
        specializarea Informatica.</p>

    <p> <b>Baza materiala moderna </b> </p>

    <p>
        Departamentul de Informatica este pozitionat în cadrul Cladirii centrale a Universitatii din Craiova
        situata în str. A. I. Cuza, nr. 13, la doi pasi de Teatrul National, asezamânt de cultura
        binecunoscut atât la nivel national cât si international (vezi <a href="https://www.google.ro/maps/place/Universitatea+din+Craiova/@44.319509,23.800905,17z/data=!3m1!4b1!4m2!3m1!1s0x4752d7a006e30d1b:0x84a5887ac48a77c7?hl=ro">harta</a>).
    </p>
    <p>Locatia este usor accesibila cu toate mijloacele de transport public (linia de tramvai, liniile de
        autobuz).</p>
    <p>Salile în care se predau cursurile de specialitate în Informatica sunt confortabile si sunt situate
        în aceeasi cladire cu birourile cadrelor didactice, facilitând interactiunea continua dintre
        studenti si profesori precum si studiul individual.
        Laboratoarele dedicate studiilor de baza si avansate sunt spatioase, moderne si bine organizate.</p>
    <p>Studenti nostri au acces la Internet atât în laboratoare cât si în salile de curs pin intermediul
        unei solutii wireless profesionale, de ultima generatie.</p>
    <p>Toti studentii din provincie au posibilitatea cazarii în caminele modernizate ale facultatii.</p>
    <p>Studentii au, de asemenea, acces la o biblioteca noua ce include un numar foarte mare de carti si
        reviste de specialitate.</p>
